inline int high_order_degree(int n_points) {
    switch (n_points) {
        case 350: return 31;
        case 434: return 35;
        case 590: return 41;
        case 770: return 47;
        case 974: return 53;
        case 1202: return 59;
        default:
            throw std::runtime_error(
                "unknown high-order Lebedev degree for grid size: " + std::to_string(n_points)
            );
    }
}

inline bool is_high_order_supported(int n_points) {
    switch (n_points) {
        case 350:
        case 434:
        case 590:
        case 770:
        case 974:
        case 1202:
            return true;
        default:
            return false;
    }
}

inline std::vector<LebedevPoint> make_high_order_grid(int n_points,
                                                      bool weights_sum_to_four_pi = true) {
    std::vector<LebedevPoint> points;
    const auto terms = high_order_rule_terms(n_points);

    for (const auto& term : terms) {
        add_rule_term(points, term);
    }

    if (static_cast<int>(points.size()) != n_points) {
        throw std::runtime_error(
            "high-order Lebedev grid generated " + std::to_string(points.size()) +
            " points, but expected " + std::to_string(n_points)
        );
    }

    double wsum = 0.0;
    for (const auto& p : points) {
        const double norm = p.u.norm();
        if (std::abs(norm - 1.0) > 1.0e-12) {
            throw std::runtime_error("high-order Lebedev point is not on the unit sphere");
        }
        wsum += p.w;
    }

    if (std::abs(wsum - 1.0) > 1.0e-12) {
        throw std::runtime_error(
            "high-order Lebedev normalized weights do not sum to 1. Sum = " +
            std::to_string(wsum)
        );
    }

    if (weights_sum_to_four_pi) {
        const double factor = 4.0 * pi();
        for (auto& p : points) {
            p.w *= factor;
        }
    }

    return points;
}

inline std::vector<LebedevPoint> make_lebedev_grid_extended(int n_points,
                                                            bool weights_sum_to_four_pi = true) {
    if (is_supported(n_points)) {
        return make_lebedev_grid(n_points, weights_sum_to_four_pi);
    }
    return make_high_order_grid(n_points, weights_sum_to_four_pi);
}

inline bool is_supported_extended(int n_points) {
    return is_supported(n_points) || is_high_order_supported(n_points);
}

